let togg1 = document.getElementById("togg1");
let hi1 = document.getElementById("hi1");
togg1.addEventListener("click", () => {
    if(getComputedStyle(hi1).display != "none"){
    hi1.style.display = "none";
    } else {
    hi1.style.display = "flex";
    }
  })

